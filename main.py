# [IMPORTS] #   

import pygame
import sys

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#

# [SETTINGS] #

pygame.init()
from tiles import Tile

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#

# [SCREEN] #

screen_width = 1200
screen_height = 800
screen = pygame.display.set_mode((screen_width, screen_height))

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#

# [INSTÃ‚NCIAS] #

tile_instance = Tile(105, 205, 50, 50, (255, 0, 255))
tile_instance2 = Tile(100, 200, 60, 60, (255, 255, 255))


#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#

# [EVENTS] #

while True:
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            pygame.quit()
            sys.exit()

    tile_instance2.draw(screen)
    tile_instance.draw(screen)

    pygame.display.flip()


#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#

# [IMPORTS] #   

import pygame
from tiles import Tile

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#

# [SETTINGS] #

pygame.init()
clock = pygame.time.Clock()

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#

# [SCREEN] #

screen_width = 1200
screen_height = 800
screen = pygame.display.set_mode((screen_width, screen_height))

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#

# [INSTÃ‚NCIAS] #

myrect = Tile(600, 400, 50, 50, (0, 0, 255), 0)

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#

# [EVENTS] #

while True:
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            pygame.quit()

    myrect.run(screen)

    pygame.display.flip()
    clock.tick(60)

